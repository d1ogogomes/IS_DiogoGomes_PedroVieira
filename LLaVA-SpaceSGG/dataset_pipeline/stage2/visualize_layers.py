import argparse
import json
import re
from pathlib import Path

import cv2


LAYER_RE = re.compile(r"Layer\s+(\d+)\s*:(.*?)(?=Layer\s+\d+\s*:|(?:\n\s*\d+\.\s*Question)|\Z)", re.DOTALL)
REF_BOX_RE = re.compile(
    r"<ref>(.*?)</ref>\s*<box>\s*\[\[(\d+)\s*,\s*(\d+)\s*,\s*(\d+)\s*,\s*(\d+)\]\]\s*</box>",
    re.DOTALL,
)

COLORS = [
    (46, 204, 113),
    (52, 152, 219),
    (231, 76, 60),
    (155, 89, 182),
    (241, 196, 15),
    (26, 188, 156),
]


def load_answer(result_file, key=None):
    with open(result_file) as file:
        data = json.load(file)

    if isinstance(data, dict):
        return data["answer"], data.get("image")

    if key:
        for item in data:
            if item.get("key") == key:
                return item["answer"], item.get("image")
        raise KeyError(f"Key not found in result file: {key}")

    if len(data) != 1:
        raise RuntimeError("Result file has multiple entries. Pass --key.")
    return data[0]["answer"], data[0].get("image")


def normalized_to_pixels(box, width, height):
    x0, y0, x1, y1 = box
    return (
        round(x0 / 999 * width),
        round(y0 / 999 * height),
        round(x1 / 999 * width),
        round(y1 / 999 * height),
    )


def visualize(image_path, answer, output_file):
    image = cv2.imread(str(image_path))
    if image is None:
        raise FileNotFoundError(f"Could not read image: {image_path}")

    height, width = image.shape[:2]
    overlay = image.copy()

    for layer_match in LAYER_RE.finditer(answer):
        layer_num = int(layer_match.group(1))
        layer_text = layer_match.group(2)
        color = COLORS[(layer_num - 1) % len(COLORS)]

        seen = set()
        for ref, x0, y0, x1, y1 in REF_BOX_RE.findall(layer_text):
            box = tuple(map(int, (x0, y0, x1, y1)))
            marker = (layer_num, ref, box)
            if marker in seen:
                continue
            seen.add(marker)

            px0, py0, px1, py1 = normalized_to_pixels(box, width, height)
            cv2.rectangle(overlay, (px0, py0), (px1, py1), color, 2)

            label = f"L{layer_num}: {ref}"
            (label_width, label_height), baseline = cv2.getTextSize(
                label, cv2.FONT_HERSHEY_SIMPLEX, 0.45, 1
            )
            label_y = max(py0 - 6, label_height + 6)
            cv2.rectangle(
                overlay,
                (px0, label_y - label_height - baseline - 4),
                (px0 + label_width + 6, label_y + baseline),
                color,
                -1,
            )
            cv2.putText(
                overlay,
                label,
                (px0 + 3, label_y - 3),
                cv2.FONT_HERSHEY_SIMPLEX,
                0.45,
                (255, 255, 255),
                1,
                cv2.LINE_AA,
            )

    output_file = Path(output_file)
    output_file.parent.mkdir(parents=True, exist_ok=True)
    cv2.imwrite(str(output_file), overlay)


def main():
    parser = argparse.ArgumentParser(description="Draw LLaVA-SpaceSGG layers over an image.")
    parser.add_argument("--result-file", required=True, help="JSON file generated by the IAedu scripts.")
    parser.add_argument("--image", help="Image path. Optional if result JSON contains image.")
    parser.add_argument("--key", help="Entry key when result JSON contains multiple entries.")
    parser.add_argument("--output-file", required=True, help="Annotated output image path.")
    args = parser.parse_args()

    answer, result_image = load_answer(args.result_file, key=args.key)
    image_path = args.image or result_image
    if not image_path:
        raise RuntimeError("Pass --image because the result file does not contain an image path.")

    visualize(Path(image_path), answer, args.output_file)
    print(f"Saved visualization to {args.output_file}")


if __name__ == "__main__":
    main()
